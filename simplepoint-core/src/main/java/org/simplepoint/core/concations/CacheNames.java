package org.simplepoint.core.concations;

/** * This interface defines the names of various caches used in the SimplePoint application.
 * Each constant represents a specific cache name, which can be used for caching internationalization
 * messages, locales, timezones, countries, and languages.
 */
public interface CacheNames {
  String SIMPLEPOINT_I18N_MESSAGES = "simplepoint:i18n:messages";
  String SIMPLEPOINT_I18N_LOCALES = "simplepoint:i18n:locales";
  String SIMPLEPOINT_I18N_TIMEZONES = "simplepoint:i18n:timezones";
  String SIMPLEPOINT_I18N_COUNTRIES = "simplepoint:i18n:countries";
  String SIMPLEPOINT_I18N_LANGUAGES = "simplepoint:i18n:languages";
}
