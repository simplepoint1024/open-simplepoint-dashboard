  // ---- constraintDefinition: CHECK and FOREIGN_KEY ----

  @Test
  void buildCreateTableSqlShouldIncludeCheckConstraint() {
    String sql = pgDialect.buildCreateTableSql(
        "\"t\"",
        List.of(new JdbcMetadataModels.ColumnDefinition("score", "INT", null, null, false, null, null, null)),
        List.of(new JdbcMetadataModels.ConstraintDefinition(
            "chk_score", JdbcMetadataModels.ConstraintType.CHECK,
            List.of(), null, "score > 0"
        )),
        pgContext()
    );
    assertTrue(sql.contains("CONSTRAINT \"chk_score\" CHECK (score > 0)"));
  }

  @Test
  void buildCreateTableSqlShouldIncludeForeignKeyConstraint() {
    List<JdbcMetadataModels.PathSegment> tablePath = List.of(
        new JdbcMetadataModels.PathSegment(JdbcMetadataModels.NodeType.SCHEMA, "public"),
        new JdbcMetadataModels.PathSegment(JdbcMetadataModels.NodeType.TABLE, "roles")
    );
    String sql = pgDialect.buildCreateTableSql(
        "\"t\"",
        List.of(new JdbcMetadataModels.ColumnDefinition("role_id", "BIGINT", null, null, false, null, null, null)),
        List.of(new JdbcMetadataModels.ConstraintDefinition(
            "fk_role", JdbcMetadataModels.ConstraintType.FOREIGN_KEY,
            List.of("role_id"),
            new JdbcMetadataModels.ConstraintReference(tablePath, List.of("id")),
            null
        )),
        pgContext()
    );
    assertTrue(sql.contains("CONSTRAINT \"fk_role\" FOREIGN KEY (\"role_id\") REFERENCES"));
    assertTrue(sql.contains("\"public\".\"roles\""));
  }

