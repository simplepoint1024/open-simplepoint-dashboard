  // ---- findColumnIndex ----

  @Test
  void findColumnIndexShouldReturnCorrectIndex() {
    List<JdbcDatabaseDialect.MetadataColumn> columns = List.of(
        new JdbcDatabaseDialect.MetadataColumn("TABLE_CAT", "VARCHAR", Types.VARCHAR),
        new JdbcDatabaseDialect.MetadataColumn("TABLE_TYPE", "VARCHAR", Types.VARCHAR)
    );
    assertEquals(1, pgDialect.findColumnIndex(columns, "TABLE_TYPE"));
    assertEquals(0, pgDialect.findColumnIndex(columns, "table_cat"));
    assertEquals(-1, pgDialect.findColumnIndex(columns, "MISSING"));
  }

