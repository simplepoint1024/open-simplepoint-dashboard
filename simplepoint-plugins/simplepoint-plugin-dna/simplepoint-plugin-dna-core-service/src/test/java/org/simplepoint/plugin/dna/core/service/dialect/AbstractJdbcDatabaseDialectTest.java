  // ---- Helper builder ----

  private static ResultSet buildStringResultSet(final String... columnNames) throws Exception {
    return buildStringResultSet(columnNames[0], columnNames.length > 1
        ? List.of()
        : List.of());
  }

  private static ResultSet buildStringResultSet(final String columnName, final List<String> values) throws Exception {
    ResultSet rs = mock(ResultSet.class);
    ResultSetMetaData rsMeta = mock(ResultSetMetaData.class);
    when(rs.getMetaData()).thenReturn(rsMeta);
    when(rsMeta.getColumnCount()).thenReturn(1);
    when(rsMeta.getColumnLabel(1)).thenReturn(columnName);
    when(rsMeta.getColumnName(1)).thenReturn(columnName);
    when(rsMeta.getColumnTypeName(1)).thenReturn("VARCHAR");
    when(rsMeta.getColumnType(1)).thenReturn(Types.VARCHAR);

    if (values.isEmpty()) {
      when(rs.next()).thenReturn(false);
    } else {
      Boolean[] trues = new Boolean[values.size()];
      java.util.Arrays.fill(trues, Boolean.TRUE);
      Boolean first = trues[0];
      Boolean[] rest = java.util.Arrays.copyOfRange(trues, 1, trues.length);
      Boolean[] withFalse = new Boolean[values.size() + 1];
      System.arraycopy(trues, 0, withFalse, 0, values.size());
      withFalse[values.size()] = Boolean.FALSE;
      when(rs.next()).thenReturn(withFalse[0], java.util.Arrays.copyOfRange(withFalse, 1, withFalse.length));
      String[] strValues = values.toArray(String[]::new);
      String firstVal = strValues[0];
      String[] restVals = java.util.Arrays.copyOfRange(strValues, 1, strValues.length);
      when(rs.getString(columnName)).thenReturn(firstVal, restVals);
      when(rs.getObject(1)).thenReturn(firstVal, (Object[]) restVals);
    }
    return rs;
  }
