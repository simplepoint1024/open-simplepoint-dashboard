  // ---- loadTableTypes ----

  @Test
  void loadTableTypesShouldNormalizeBaseTableToTable() throws Exception {
    DatabaseMetaData metaData = mock(DatabaseMetaData.class);
    ResultSet tableTypesResult = buildStringResultSet("TABLE_TYPE", List.of("BASE TABLE", "VIEW", "TABLE"));
    when(metaData.getTableTypes()).thenReturn(tableTypesResult);

    MetadataResult result = pgDialect.loadTableTypes(null, metaData, pgContext());
    List<String> types = result.rows().stream()
        .map(row -> (String) row.get(0))
        .distinct()
        .toList();
    assertTrue(types.contains("TABLE"));
    assertFalse(types.contains("BASE TABLE"));
  }

